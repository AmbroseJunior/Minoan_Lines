fuel_router = APIRouter(prefix="/compliance", tags=["compliance"])


@fuel_router.post("/fuel-data", status_code=201)
@sync_limit
async def ingest_fuel_data(request: Request, payload: FuelDataIngest):
    """Ingest a fuel consumption record from vessel SCADA. Idempotent."""
    idempotency_key = payload.idempotency_key or f"{payload.vessel_name}_{payload.voyage_id}"

    # Idempotency check
    existing = (
        db().table("fuel_consumption_records")
        .select("id")
        .eq("idempotency_key", idempotency_key)
        .execute()
    )
    if existing.data:
        return {"message": "Record already exists (idempotent)", "idempotency_key": idempotency_key}

    factor = EMISSION_FACTORS.get(payload.fuel_type.upper(), 3.206)
    co2 = payload.fuel_consumed_mt * factor

    record = {
        "id": str(uuid.uuid4()),
        "vessel_name": payload.vessel_name,
        "voyage_id": payload.voyage_id,
        "departure_port": payload.departure_port,
        "arrival_port": payload.arrival_port,
        "departure_time": payload.departure_time.isoformat(),
        "arrival_time": payload.arrival_time.isoformat(),
        "distance_nm": payload.distance_nm,
        "fuel_type": payload.fuel_type,
        "fuel_consumed_mt": payload.fuel_consumed_mt,
        "co2_emission_factor": factor,
        "co2_emissions_mt": co2,
        "is_eu_ets_voyage": payload.is_eu_ets_voyage,
        "idempotency_key": idempotency_key,
    }

    db().table("fuel_consumption_records").insert(record).execute()
    log.info("compliance.fuel_ingested", vessel=payload.vessel_name, voyage=payload.voyage_id, co2=co2)

    return {"message": "Fuel record ingested", "voyage_id": payload.voyage_id, "co2_mt": co2}


@fuel_router.get("/fuel-data")
@default_limit
async def list_fuel_data(
    request: Request,
    vessel_name: Optional[str] = Query(None),
    limit: int = Query(50, ge=1, le=200),
    offset: int = Query(0, ge=0),
):
    """List fuel consumption records with optional vessel filter."""
    query = (
        db().table("fuel_consumption_records")
        .select("*")
        .order("departure_time", desc=True)
        .range(offset, offset + limit - 1)
    )
    if vessel_name:
        query = query.eq("vessel_name", vessel_name)

    res = query.execute()
    return res.data or []


# ─── Helpers ─────────────────────────────────────────────────────────────────

def _aggregate_fuel_data_from_supabase(
    vessel_name: Optional[str],
    period: str,
) -> dict[str, Any]:
    """Aggregate fuel records from Supabase for the reporting period."""
    try:
        query = db().table("fuel_consumption_records").select("fuel_consumed_mt,co2_emissions_mt,is_eu_ets_voyage")
        if vessel_name:
            query = query.eq("vessel_name", vessel_name)
        res = query.execute()
        records = res.data or []

        total_fuel = sum(r.get("fuel_consumed_mt") or 0 for r in records)
        total_co2 = sum(r.get("co2_emissions_mt") or 0 for r in records)
        ets_coverage = 1.0

        return {
            "fuel_type": "HFO",
            "fuel_consumed_mt": total_fuel,
            "co2_emission_factor": 3.206,
            "co2_emissions_mt": total_co2,
            "voyage_type": "intra_eu",
            "ets_coverage_pct": int(ets_coverage * 100),
            "ets_allowances_required": total_co2 * ets_coverage,
            "ets_price_per_tonne_eur": 65.0,
            "estimated_ets_cost_eur": total_co2 * ets_coverage * 65.0,
        }
    except Exception as exc:
        log.error("compliance.aggregate_error", error=str(exc))
        return {
            "fuel_type": "HFO",
            "fuel_consumed_mt": 0.0,
            "co2_emissions_mt": 0.0,
            "ets_allowances_required": 0.0,
            "estimated_ets_cost_eur": 0.0,
        }